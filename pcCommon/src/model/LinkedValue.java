package model;

public class LinkedValue {
	private int idsignal;
	private double val;
	
	public LinkedValue() {
		
	}

	public int getIdsignal() {
		return idsignal;
	}

	public void setIdsignal(int idsignal) {
		this.idsignal = idsignal;
	}

	public double getVal() {
		return val;
	}

	public void setVal(double val) {
		this.val = val;
	}

}
